package strata.foundation.core.stream;

public abstract
class AbstractUnboundedStreamSink<T>
    implements IUnboundedStreamSink<T>
{
    @Override
    public void
    accept(IUnboundedStream<T> stream)
    {
        stream.forEach(e -> process(e));
    }

    protected abstract void
    process(T element);
}

